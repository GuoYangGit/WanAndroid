object NavigationUtil {
    fun toWebActivity(url: String, title: String) {
        ARouter.getInstance().build(RouterConstants.WEB_WEBACTIVITY)
                .withString("url", url)
                .withString("title", title)
                .navigation()
    }

    fun toMainActivity() {
        ARouter.getInstance().build(RouterConstants.WAN_MAINACTIVITY)
                .navigation()
    }
}